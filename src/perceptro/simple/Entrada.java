
package perceptro.simple;

public class Entrada {
    private double peso;
    private double valor;
    public Entrada(double valor,double peso){
        this.peso=peso;
        this.valor=valor;
    }
    
    public double getPeso(){
        return peso;
    }
    
    public double getValor(){
        return valor;
    }
    
    
}
